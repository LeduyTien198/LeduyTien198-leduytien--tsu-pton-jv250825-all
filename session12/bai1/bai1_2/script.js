//  học sinh 
// đó không đủ điều kiện vào học lớp 10 và 
// hiển thị ra thông báo bằng console hoặc alert.

let age = parseInt(prompt("nhập tuổi học sinh"));
if (age >= 15) {
    console.log("đủ điều kiện nhập học")
} else {
    console.log("không đủ điều, nếu đủ điều kiện hãy đến trực tiếp trường bạn đăng ký")
}

